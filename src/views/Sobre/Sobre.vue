      <h1>O que é COVID-19</h1>

      <p>
        Os coronavírus são uma grande família de vírus comuns em muitas espécies
        diferentes de animais, incluindo camelos, gado, gatos e morcegos.
        Raramente, os coronavírus que infectam animais podem infectar pessoas,
        como exemplo do MERS-CoV e SARS-CoV. Recentemente, em dezembro de 2019,
        houve a transmissão de um novo coronavírus (SARS-CoV-2), o qual foi
        identificado em Wuhan na China e causou a COVID-19, sendo em seguida
        disseminada e transmitida pessoa a pessoa.
      </p>

      <p>
        A COVID-19 é uma doença causada pelo coronavírus, denominado SARS-CoV-2,
        que apresenta um espectro clínico variando de infecções assintomáticas a
        quadros graves. De acordo com a Organização Mundial de Saúde, a maioria
        (cerca de 80%) dos pacientes com COVID-19 podem ser assintomáticos ou
        oligossintomáticos (poucos sintomas), e aproximadamente 20% dos casos
        detectados requer atendimento hospitalar por apresentarem dificuldade
        respiratória, dos quais aproximadamente 5% podem necessitar de suporte
        ventilatório.
      </p>

      <h1>Quais são os sintomas</h1>

      <p>
        Os sintomas da COVID-19 podem variar de um resfriado, a uma Síndrome
        Gripal-SG (presença de um quadro respiratório agudo, caracterizado por,
        pelo menos dois dos seguintes sintomas: sensação febril ou febre
        associada a dor de garganta, dor de cabeça, tosse, coriza) até uma
        pneumonia severa. Sendo os sintomas mais comuns:
      </p>

      <ul>
        <li>Tosse</li>
        <li>Febre</li>
        <li>Coriza</li>
        <li>Dor de garganta</li>
        <li>Dificuldade para respirar</li>
        <li>Perda de olfato (anosmia)</li>
        <li>Alteração do paladar (ageusia)</li>
        <li>Distúrbios gastrintestinais (náuseas/vômitos/diarreia)</li>
        <li>Cansaço (astenia)</li>
        <li>Diminuição do apetite (hiporexia)</li>
        <li>Dispnéia ( falta de ar)</li>
      </ul>

      <h1>Como é transmitido</h1>

      <p>
        A transmissão acontece de uma pessoa doente para outra ou por contato
        próximo por meio de:
      </p>

      <ul>
        <li>Toque do aperto de mão contaminadas;</li>
        <li>Gotículas de saliva;</li>
        <li>Espirro;</li>
        <li>Tosse;</li>
        <li>Catarro;</li>
        <li>
          Objetos ou superfícies contaminadas, como celulares, mesas, talheres,
          maçanetas, brinquedos, teclados de computador etc.
        </li>
      </ul>

      <h1>Diagnóstico</h1>

      <p>
        <strong
          >O diagnóstico da COVID-19 pode ser realizado a partir de critérios
          como:</strong
        >
      </p>

      <p>
        1 - O <strong>DIAGNÓSTICO CLÍNICO</strong> é realizado pelo médico
        atendente, que deve avaliar a possibilidade da doença, principalmente,
        em pacientes com a associação dos seguintes sinais e sintomas:
      </p>

      <ul>
        <li>
          Febre, que pode estar presente no momento do exame clínico ou referida
          pelo paciente (sensação febril) de ocorrência recente.
        </li>
        <li>
          Sintomas do trato respiratório (por exemplo, tosse, dispneia, coriza,
          dor de garganta)
        </li>
        <li>
          Outros sintomas consistentes incluindo, mialgias, distúrbios
          gastrointestinais (diarreia/náuseas/vômitos), perda ou diminuição do
          olfato (anosmia) ou perda ou diminuição do paladar (ageusia).
        </li>
      </ul>

      <p>
        Em crianças, além dos itens anteriores, considera-se também a obstrução
        nasal, a desidratação e a falta de apetite (inapetência), na ausência de
        outro diagnóstico específico. Em idosos, deve-se considerar também,
        critérios específicos de agravamento como: síncope (desmaio ou perda
        temporária de consciência), confusão mental, sonolência excessiva,
        irritabilidade e falta de apetite (inapetência).
      </p>

      <p>
        O diagnóstico clínico da doença, também deve ser considerado em
        pacientes com doença grave do trato respiratório inferior sem causa
        clara, como é o caso de pacientes que se apresentem em Síndrome
        Respiratória Aguda Grave (SRAG). Nesta síndrome o indivíduo apresenta-se
        em franca dispneia/desconforto respiratório/dificuldade para respirar
        com saturação de oxigênio (O2) menor do que 95% em ar ambiente ou
        coloração azulada dos lábios ou rosto (cianose) ou queixa de pressão
        persistente no tórax. Em crianças, a SRAG apresenta-se com os sinais e
        sintomas anteriores, devendo ser observados sinais característicos de
        esforço respiratório, tais como, os batimentos de asa de nariz, tiragem
        intercostal, e, por fim, alteração na coloração das extremidades que
        ficam azuladas (cianose).
      </p>

      <p>
        2 - O <strong>DIAGNÓSTICO CLÍNICO-EPIDEMIOLÓGICO</strong> é realizado
        pelo médico atendente no qual considera-se:
      </p>
      <ul>
        <li>
          casos de paciente com a associação dos sinais e sintomas supracitados
          ou SRAG MAIS histórico de contato próximo ou domiciliar, nos últimos
          14 dias antes do aparecimento dos sintomas, com caso confirmado
          laboratorialmente para COVID-19 e para o qual não foi possível
          realizar a investigação laboratorial específica.
        </li>
      </ul>

      <p>3 -<strong>DIAGNÓSTICO CLÍNICO-IMAGEM:</strong></p>

      <ul>
        <li>
          caso de sintomas respiratório mais febre ou SRAG ou óbito por SRAG que
          não foi possível confirmar ou descartar por critério laboratorial E
          que apresente alterações tomográficas.
        </li>
      </ul>

      <p>
        4 - <strong>DIAGNÓSTICO LABORATORIAL</strong> - Caso o paciente
        apresente os sintomas respiratórios mais febre ou SRAG. O profissional
        de saúde poderá solicitar os seguintes exames laboratoriais:
      </p>

      <ul>
        <li>
          De biologia molecular, (RT-PCR em tempo real) que diagnostica tanto a
          COVID-19, a Influenza ou a presença de Vírus Sincicial Respiratório
          (VSR) normalmente até o oitavo dia de início de sintomas.
        </li>
        <li>
          Imunológico, que detecta, ou não, a presença de anticorpos em amostras
          coletadas a partir do oitavo dia de início dos sintomas. Sendo eles:
        </li>
        <li>
          Ensaio imunoenzimático (Enzyme-Linked Immunosorbent Assay - ELISA);
        </li>
        <li>Imunocromatografia (teste rápido) para detecção de anticorpos;</li>
        <li>Imunoensaio por Eletroquimioluminescência (ECLIA).</li>
        <li>
          Pesquisa de antígenos: resultado reagente para SARS-CoV-2 pelo método
          de Imunocromatografia para detecção de antígeno.
        </li>
      </ul>

      <p>
        5 -
        <strong>DIAGNÓSTICO LABORATORIAL EM INDIVÍDUO ASSINTOMÁTICO</strong>
        (pessoa sem sintomas) que realizou:
      </p>

      <ul>
        <li>
          Exame de Biologia Molecular com resultado DETECTÁVEL para SARS-CoV-2
          realizado pelo método RT-PCR em tempo real.
        </li>
        <li>
          Exame de Imunológico com resultado REAGENTE para IgM e/ou IgA
          realizado pelos seguintes métodos: Ensaio imunoenzimático (ELISA) e
          Imunocromatografia (teste rápido) para detecção de anticorpos.
        </li>
      </ul>

      <h1>Como se proteger</h1>
      <p>As recomendações de prevenção à COVID-19 são as seguintes:</p>
      <ul>
        <li>
          Lave com frequência as mãos até a altura dos punhos, com água e sabão,
          ou então higienize com álcool em gel 70%. Essa frequência deve ser
          ampliada quando estiver em algum ambiente público (ambientes de
          trabalho, prédios e instalações comerciais, etc), quando utilizar
          estrutura de transporte público ou tocar superfícies e objetos de uso
          compartilhado.
        </li>
        <li>
          Ao tossir ou espirrar, cubra nariz e boca com lenço ou com a parte
          interna do cotovelo.
        </li>
        <li>
          Não tocar olhos, nariz, boca ou a máscara de proteção fácil com as
          mãos não higienizadas.
        </li>
        <li>
          Se tocar olhos, nariz, boca ou a máscara, higienize sempre as mãos
          como já indicado.
        </li>
        <li>
          Mantenha distância mínima de 1 (um) metro entre pessoas em lugares
          públicos e de convívio social. Evite abraços, beijos e apertos de
          mãos. Adote um comportamento amigável sem contato físico, mas sempre
          com um sorriso no rosto.
        </li>
        <li>
          Higienize com frequência o celular, brinquedos das crianças e outro
          objetos que são utilizados com frequência.
        </li>
        <li>
          Não compartilhe objetos de uso pessoal como talheres, toalhas, pratos
          e copos.
        </li>
        <li>Mantenha os ambientes limpos e bem ventilados.</li>
        <li>
          Se estiver doente, evite contato próximo com outras pessoas,
          principalmente idosos e doentes crônicos, busque orientação pelos
          canais on-line disponibilizados pelo SUS ou atendimento nos serviços
          de saúde e siga as recomendações do profissional de saúde.
        </li>
        <li>Durma bem e tenha uma alimentação saudável.</li>
        <li>
          Recomenda-se a utilização de máscaras em todos os ambientes. As
          máscaras de tecido (caseiras/artesanais), não são Equipamentos de
          Proteção Individual (EPI), mas podem funcionar como uma barreira
          física, em especial contra a saída de gotículas potencialmente
          contaminadas.
        </li>
      </ul>

      <p>
        Estimule familiares, amigos e colegas de trabalho sobre a importância do
        uso de máscara e da higienização das mãos na prevenção da disseminação
